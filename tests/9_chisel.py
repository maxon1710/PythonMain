# список из 9 чисел
numbers = [3, 17, 5, 42, 8, 1, 94, 23, 7]

# берём первое число как стартовый максимум
max_num = numbers[0]

# проходим по каждому числу в списке
for num in numbers:
    if num > max_num:  # если текущее число больше максимума
        max_num = num  # обновляем максимум

print(f"Максимальное число: {max_num}")  # 94